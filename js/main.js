document.querySelector('.add-1').addEventListener('click', function() {
    const input = document.querySelector('.pro-no-order');
    input.value = parseInt(input.value) + 1;
});

document.querySelector('.remove-1').addEventListener('click', function() {
    const input = document.querySelector('.pro-no-order');
    const currentValue = parseInt(input.value);
    if (currentValue > 1) {
        input.value = currentValue - 1;
    }
});

document.querySelectorAll('details').forEach((detail) => {
  detail.addEventListener('toggle', (event) => {
    const content = event.target.querySelector('.details-content');
    if (detail.open) {
      content.style.maxHeight = content.scrollHeight + 'px';
    } else {
      content.style.maxHeight = '1000';
    }
  });
});


