let copyBtn = document.getElementById('copy');
let anyThing = document.getElementById('right');

copyBtn.addEventListener('click', function() {
    navigator.clipboard.writeText(right.textContent).then(function() {
        copyBtn.textContent = "Copied";
        anyThing.innerHTML = `<i class="fa-solid fa-check"></i>`;
        right.style.borderRight = "none";
        setTimeout(function() {
            copyBtn.innerHTML = '<i class="fa-regular fa-copy"></i>' + " " + 'Copy Code';
        }, 1500);

        setTimeout(function() {
            anyThing.textContent = 'EXTRA7';
            right.style.borderRight = "1px dashed rgb(230, 230, 230)";
        }, 1500);
    }).catch(function(error) {
        console.error('Could not copy text: ', error);
    });
});

